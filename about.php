<p>Signet Jewellers is the largest retailer of fine jewellery in Canada, with nearly 150 locations from the Maritimes to British Columbia. As a mall-based retailer in prime locations, Signet provides an accessible and comfortable shopping experience for its Canadian consumers. With its strong assortment of affordable fine jewellery and brand‐name watches, Signet Jewellers provides selections for every occasion and every customer.<br><br>

Founded in 1919 as a family business, Signet Jewellers was the first company in Canada to sell jewellery on a time payment plan. From this modest beginning, the company grew to a national business, and by 1957 was the largest credit jeweller in the British Commonwealth. Today, Signet Jewellers continues to offer its customer a wide variety of credit programs.<br><br>

Signet Jewellers has also become known as "the diamond store" for Canadian consumers. Focused on providing choices to customers who seek excellent value in fine jewellery, Signet designs much of its jewellery. <br><br>

